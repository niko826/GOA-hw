# for ციკლი გამოიყენება სიებში, სტრინგებში, დიაპაზონებში (range) და სხვა იტერირებად ობიექტებში გასასვლელად.
# ის ავტომატურად იღებს თითოეულ ელემენტს მოცემული ობიექტიდან და ასრულებს შესაბამის კოდს.

# მაგალითი: 
for i in range(1, 6):  # range(1, 6) ქმნის რიცხვების სიას: [1, 2, 3, 4, 5]
    print(i)  # თითოეული რიცხვი იბეჭდება ცალ-ცალკე