function CARS(car) {
  if (car.toLowerCase() === "მერსედესი") {
    console.log("კარგი არჩევანია");
  } else if (car.toLowerCase() === "ტესლა") {
    console.log("ცუდი არჩევანია");
  } else if (car.toLowerCase() === "ოპელი") {
    console.log("საშუალო არჩევანია");
  } else {
    console.log("ვერ გეტყვი");
  }
}





