#for ციკლი


#გამოიყენება, როცა წინასწარ იცი, რამდენჯერ უნდა შესრულდეს ციკლი — მაგალითად, სიაში ან დიაპაზონში გადაადგილებისას.


#მაგალითი:

for i in range(5):
    print(i)



#while ციკლი


#შესრულდება მანამ, სანამ პირობა ჭეშმარიტია. გამოიყენება, როცა არ იცი ზუსტად რამდენჯერ შესრულდება ციკლი.

#მაგალითი:

x = 0

while x < 5:
    print(x)
    x += 1

#for — იცი რამდენჯერ უნდა გაიმეორო.

#while — არ იცი რამდენჯერ, მანამ მიმდინარეობს, სანამ პირობა ჭეშმარიტია.



