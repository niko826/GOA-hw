# Indexing ნიშნავს ელემენტებზე წვდომას მათი მდებარეობის (ნომრის) მიხედვით.
# ყველა ელემენტს სიაში, სტრიქონში ან სხვა კოლექციაში აქვს ინდექსი (Index),
# რომელიც იწყება 0-დან.

# მაგალითად:
text = "Python"

# ინდექსი 0 — პირველი სიმბოლოა
print(text[0])  # შედეგი: P

# ინდექსი 1 — მეორე სიმბოლო
print(text[1])  # შედეგი: y

# შეგვიძლია ვიყენოთ უარყოფითი ინდექსიც:
# -1 ნიშნავს ბოლო სიმბოლოს
print(text[-1])  # შედეგი: n

# Index-ის მნიშვნელობაა: ის წარმოადგენს ელემენტის პოზიციას კოლექციაში.
# Indexing საშუალებას გაძლევს აირჩიო კონკრეტული ელემენტი, მაგალითად სიის ან სტრიქონის შიგნით.
